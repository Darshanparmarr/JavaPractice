public class Variables {
    public static void main(String[] args) {
        int myNumber = 865;
        int yourNumber;

        yourNumber = 100;
        System.out.println(yourNumber);

        yourNumber = 1000;
        System.out.println(yourNumber);

        float myFloat = 10.1f;
        System.out.println(myFloat);

        double myDouble = 10.1314121;
        System.out.println(myDouble);

        boolean isAmbrish = false;
        System.out.println(isAmbrish);

        String name = "Hello Mumbai...!";
        System.out.println(name);
    }
}
